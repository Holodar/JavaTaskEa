package taskCats1;

public class Cats {


    private String name;
    private String gender;
    private int age;

    public String getName() {
        return name;
    }

    public String getGender() {
        return gender;
    }

    public int getAge() {
        return age;
    }

    public Cats(String name, String gender, int age) {
        this.name = name;
        this.age = age;
        this.gender = gender;
    }
}




